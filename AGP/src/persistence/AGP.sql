#------------------------------------------------------------
# Table: TOURISTIC_SITE
#------------------------------------------------------------

CREATE TABLE TOURISTIC_SITE(
        ID_SITE   int (11) Auto_increment  NOT NULL ,
        NAME      Varchar (25) NOT NULL ,
        PRICE     Int NOT NULL ,
        PICTURE   Blob ,
        TYPE      Varchar (25) NOT NULL ,
        ADDRESS   Varchar (25) ,
        DURATION int(8),
        LONGITUDE BigInt ,
        LATITUDE  BigInt ,
        PRIMARY KEY (ID_SITE )
)ENGINE=InnoDB;


#------------------------------------------------------------
# Table: HOTEL
#------------------------------------------------------------

CREATE TABLE hotel(
        ID_HOTEL  int (11) Auto_increment  NOT NULL ,
        HRANGE    Int NOT NULL ,
        PRICE     Int NOT NULL ,
        PICTURE   Blob ,
        NAME      Varchar (25) NOT NULL ,
        ADDRESS   Varchar (25) ,
        LONGITUDE BigInt ,
        LATITUDE  BigInt ,
        PRIMARY KEY (ID_HOTEL )
)ENGINE=InnoDB;


#------------------------------------------------------------
# Table: EXCURSION
#------------------------------------------------------------

CREATE TABLE EXCURSION(
        ID_EXCURSION int (11) Auto_increment  NOT NULL ,
        NAME         Varchar (25) NOT NULL ,
        PRICE        Int NOT NULL ,
        CONFORT      Int NOT NULL ,
        ID_TRANSPORT Int NOT NULL ,
        PRIMARY KEY (ID_EXCURSION )
)ENGINE=InnoDB;


#------------------------------------------------------------
# Table: TRANSPORT
#------------------------------------------------------------

CREATE TABLE transport(
        ID_TRANSPORT int (11) Auto_increment  NOT NULL ,
		ID_SITE   int (11),
		ID_HOTEL   int (11),
        MODE         Varchar (200) ,
        DURATION     Int NOT NULL ,
        PRIMARY KEY (ID_TRANSPORT )
)ENGINE=InnoDB;


